

WITH source_data AS (
    WITH raw_stg AS (
        SELECT * FROM DEV_DFHPMS2EU_DB.PUBLIC.STG_PRODUCT_VEEVA
    ),
    rejects AS (
        SELECT DATASET_PRODUCT_ID
        FROM DEV_DFHPMS2EU_DB.PUBLIC.REJECT
    ),
    clean_stg AS (
        SELECT s.*
        FROM raw_stg s
        LEFT JOIN rejects r
            ON upper(trim(s.dataset_product_id)) = r.DATASET_PRODUCT_ID
        WHERE r.DATASET_PRODUCT_ID IS NULL
    )
    SELECT 
        *,
        CURRENT_TIMESTAMP() AS UPDATED_DATETIME
    FROM clean_stg
)

SELECT
    s.IDENTIFIER_MD5,
    s.RECORD_MD5,
    
    -- If it's a new record, START_DATETIME is now. 
    -- If it exists, the 'merge' strategy will not touch this column (it's immutable in dbt merge by default for missing columns in update set, but to be safe we redefine it).
    CURRENT_TIMESTAMP() AS START_DATETIME, 
    CURRENT_TIMESTAMP() AS LOAD_DATETIME,
    NULL::TIMESTAMP_LTZ AS END_DATETIME,
    s.UPDATED_DATETIME,
    s.ETL_BATCH_ID,
    'VEEVA_CRM' AS RECORD_SOURCE,
    
    -- ALL MAPPED COLUMNS
    s.GEOGRAPHIC_ID, s.DATASET_ID, s.SOURCE_DATASET_ID, s.DATASET_PRODUCT_ID, s.PRODUCT_TYPE_CODE,
    s.PRODUCT_NAME, s.STANDARDIZED_PRODUCT_NAME, s.DESCRIPTION, s.PRODUCT_STATUS_CODE, s.EFFECTIVE_DATE,
    s.END_DATE, s.BRAND_NAME, s.GENERIC_NAMES, s.APPROVED_DATE, s.MARKETING_START_DATE,
    s.MARKETING_END_DATE, s.EXPIRATION_DATE, s.SUBSTANCE, s.DOSE_FORM_CODE, s.STRENGTH,
    s.ROUTE_OF_ADMINISTRATION_CODE, s.PHARMACEUTICAL_PRODUCT_QUANTITY, s.UNIT_OF_PRESENTATION_CODE, s.PRIMARY_PACKAGE_QUANTITY, s.PRIMARY_PACKAGE_TYPE_CODE,
    s.SECONDARY_PACKAGE_QUANTITY, s.SECONDARY_PACKAGE_TYPE_CODE, s.PACKAGE_QUANTITY, s.SHELF_LIFE_QUANTITY, s.SHELF_LIFE_UNIT_OF_MEASURE_CODE,
    s.SAMPLE_INDICATOR, s.COMPETITOR_INDICATOR, s.GENERIC_INDICATOR, s.PRESCRIPTION_REQUIRED_INDICATOR, s.ATC_CODE,
    s.WHO_CODE, s.NFC_CODE, s.CHC_NEC_CODE, s.USC_CODE, s.MANUFACTURER_ID,
    s.MANUFACTURER, s.CORPORATION, s.DAILY_DOSAGE_QUANTITY, s.IS_IMPORTED, s.GENERIC_PRODUCT_DESCRIPTION,
    s.CHANNEL, s.CHC_FLAG

FROM source_data s

