lookup_iso AS (
  SELECT 
    r.*, 
    c."ISO2_CODE" AS "ISO_GEO_ID" 
  FROM 
    raw_union r 
    LEFT JOIN {{ ref('dim_country') }} c ON UPPER(
      TRIM(r.COUNTRY)
    ) = UPPER(
      TRIM(c.NAME)
    )
), 
cleaned_union AS (
  SELECT 
    *, 
    COALESCE(ATC1, INTWHOATC1) AS CANONICAL_ATC1,
    COALESCE(ATC2, INTWHOATC2) AS CANONICAL_ATC2,
    COALESCE(ATC3, INTWHOATC3) AS CANONICAL_ATC3,
    COALESCE(ATC4, INTWHOATC4) AS CANONICAL_ATC4,
    CASE
      WHEN INTPCK IS NULL THEN NULL
      WHEN SEMI_ETHICAL IS NOT NULL THEN CASE WHEN UPPER(SEMI_ETHICAL) LIKE '%PRESCRIPTION MEDICINE%' THEN 'Y' ELSE 'N' END
      WHEN INTRX IS NOT NULL THEN CASE WHEN UPPER(INTRX) LIKE '%PRESCRIPTION%' THEN 'Y' ELSE 'N' END
      ELSE 'N'
    END AS PRESCRIPTION_REQUIRED_INDICATOR 
  FROM 
    lookup_iso
), 
brands AS (
  SELECT 
    DISTINCT MD5(
      CONCAT("ISO_GEO_ID", DS_NM, INTPRD)
    ) AS BUSINESS_KEY, 
    "ISO_GEO_ID" AS GEOGRAPHIC_ID, 
    'IQVIA_MIDAS' AS DATASET_ID, 
    DS_NM AS SOURCE_DATASET_ID, 
    COUNTRY AS SOURCE_COUNTRY_NAME,
    CTY AS SOURCE_COUNTRY_CODE,
    'Brand' AS PRODUCT_TYPE_CODE, 
    INTPRD AS PRODUCT_NAME, 
    INTPRD AS BRAND_NAME, 
    INTPRD AS SOURCE_PRODUCT_BRAND_ID, 
    NULL AS SOURCE_PRODUCT_PACK_ID, 
    SEMI_ETHICAL, 
    NULL AS SUBSTANCE, 
    CANONICAL_ATC1 AS ATC1, 
    CANONICAL_ATC2 AS ATC2, 
    CANONICAL_ATC3 AS ATC3, 
    CANONICAL_ATC4 AS ATC_CODE, 
    CANONICAL_ATC4 AS ATC4, 
    ATC4_DESCR,
    SUBSTR(INTWHOATC5, 1, 7) AS WHO_CODE, 
    NULL AS NFC_CODE, 
    CRP AS CORPORATION, 
    CASE WHEN UPPER(CRP) LIKE '%BOEHRINGER%' THEN 'N' ELSE 'Y' END AS COMPETITOR_INDICATOR, 
    NULL AS PRESCRIPTION_REQUIRED_INDICATOR, 
    MNF AS MANUFACTURER_CODE,
    INTMNF AS MANUFACTURER, 
    NULL AS STRENGTH, 
    NULL AS DOSE_FORM_CODE 
  FROM 
    cleaned_union 
  WHERE 
    INTPRD IS NOT NULL
), 
