  SELECT 
    DISTINCT MD5(
      CONCAT(
        "ISO_GEO_ID", DS_NM, INTPRD, INTPCK
      )
    ) AS BUSINESS_KEY, 
    "ISO_GEO_ID" AS GEOGRAPHIC_ID, 
